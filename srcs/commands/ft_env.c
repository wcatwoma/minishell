#include "./../header.h"

void	ft_env(t_env *env)
{
	int k;

	k = -1;
	while (env->envp[++k])
	{
		if (ft_strchr(env->envp[k], '='))
			ft_putendl_fd(env->envp[k], 1);
	}
}
